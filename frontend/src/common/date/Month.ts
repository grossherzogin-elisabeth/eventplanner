/**
 * Months in JS Date objects are indexed based, so 1 is february not january. This enum is meant to
 * prevent confusion when reading code with hard coded dates.
 */
export enum Month {
    JANUARY,
    FEBRUARY,
    MARCH,
    APRIL,
    MAY,
    JUNE,
    JULY,
    AUGUST,
    SEPTEMBER,
    OCTOBER,
    NOVEMBER,
    DECEMBER,
}
